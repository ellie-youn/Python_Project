
# 파이썬 터틀 모듈을 가져온다
import turtle

# 그림을 그리기 위해서 캔버스(그리는 공간)을 불러온다
t = turtle.Pen()

# 캔버스 마우스 형태를 거북이 모양으로 바꾼다.
t.shape("turtle")

# 펜의 색상을 파란색으로 설정
t.pencolor('blue')

# 직선으로 100픽셀만큼 그린다
t.forward(100)

# 오른쪽으로 90도만큼 방향 변경한다
t.right(90)
t.forward(100)

